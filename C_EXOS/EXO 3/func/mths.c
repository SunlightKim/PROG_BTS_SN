#include "headers/mths.h"

// Add two numbers using mths add
int add(int a, int b) 
{
    return a + b;
}

// Subtract two numbers using mths sub
int sub(int a, int b) 
{
    return a - b;
}

// Multiply two numbers using mths mul
int mul(int a, int b) 
{
    return a * b;
}